#include"libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*ptr1;
	const char	*ptr2;

	ptr1 = dst;
	ptr2 = src;
	if (!dst && !src)
		return (NULL);
	while (n-- > 0)
		*(ptr1++) = *(ptr2++);
	return (dst);
}
